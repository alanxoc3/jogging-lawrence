class Map(object):
	def __init__(self, width, height):
		self.width = width
		self.height = height
		self._map = [0 for x in xrange(width) for y in xrange(height)]
		self.tileset = graphics.load_image(os.path.join("img", "dungeon tileset calciumtrice.png"))
		
	def __getitem__(self, index):
		x, y = index
		return self._map[x + y * self.width]
		
	def __setitem__(self, index, value):
		x, y = index
		self._map[x + y * self.width] = value


